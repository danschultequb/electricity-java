package qub;

/**
 * The different units of electical voltage measurement (aka. electrical pressure or electrical
 * potential energy).
 */
public enum VoltageUnit
{
    /**
     * 1/1,000th (0.001) of a volt.
     */
    Millivolts,

    /**
     * 1 volt.
     */
    Volts,

    /**
     * 1,000 volts.
     */
    Kilovolts,

    /**
     * 1,000,000 volts.
     */
    Megavolts,
}
